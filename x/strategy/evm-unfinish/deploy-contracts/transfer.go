func transfer(clients *common.ClientManager, contractManager *tools.ContractManager) {
	clients = common.NewClientManagerWithMode(common.GlobalConfig.Networks[""].Hosts, "0.05okt", types.BroadcastSync,50000000)
	contractManager.List()

	s, f := tools.NewCounter(0), tools.NewCounter(0)
	ethAddrs := convertCosmosAddrsToEthAddrs(backupAddrs)
	var wg sync.WaitGroup
	for i := 0; i < TransferGoNum; i++ {
		wg.Add(1)
		go func() {
			defer wg.Done()

			for {
				toEthAddr := getOneRandomEthAddr(ethAddrs)
				contract := contractManager.GetOneRandomUSDTOwner()
				addrStr := contract.Owner.GetAddress().String()
				addr, err := sdk.AccAddressFromBech32(addrStr)
				if err != nil {
					panic(err)
				}
				fromEthAddr,_ := utils.ToHexAddress(addr.String())

				cli := clients.GetClient()

				acc, err := cli.Auth().QueryAccount(addrStr)
				if err != nil {
					log.Println(err)
					continue
				}

				payload := USDT.BuildUSDTTransferPayload(toEthAddr, 1)
				_, err = cli.Evm().SendTx(contract.Owner, common.PassWord, contract.ContractAddr, "", ethcommon.Bytes2Hex(payload), "", acc.GetAccountNumber(), acc.GetSequence())
				if err != nil {
					log.Printf("[failed tx: %d] %s fail to send 10usdt to %s from contract %s: %s \n", f.Add(), fromEthAddr, toEthAddr, contract.ContractAddr, err)
					continue
				}
				log.Printf("[successful tx: %d] %s send 1usdt to %s from contract %s \n", s.Add(), fromEthAddr, toEthAddr, contract.ContractAddr)
			}
		}()
	}
	wg.Wait()
}

func getOneRandomEthAddr(ethAddrs []string) string {
	rand.Seed(time.Now().UnixNano())
	length := len(ethAddrs)
	return ethAddrs[rand.Intn(length)]
}

func convertCosmosAddrsToEthAddrs(addrStrs []string) []string {
	ethAddrs := make([]string, len(addrStrs))
	for i, addrStr := range addrStrs {
		addr, err := sdk.AccAddressFromBech32(addrStr)
		if err != nil {
			panic(err)
		}

		ethAddr, _ := utils.ToHexAddress(addr.String())
		ethAddrs[i] = ethAddr.String()
	}
	return ethAddrs
}

